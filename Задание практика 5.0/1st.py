# Импорт библиотеки requests для выполнения HTTP-запросов
import requests


# Определение функции для отправки HTTP GET-запроса
def send_request(url):
    # Выполнение GET-запроса к указанному URL и сохранение ответа в переменную response
    response = requests.get(url)
    
    # Проверка статус-кода ответа сервера
    # Код 200 означает успешное выполнение запроса (OK)
    if response.status_code == 200:
        # Если запрос успешен, возвращаем данные ответа в формате JSON
        # Метод .json() преобразует JSON-данные в словарь Python
        return response.json()
    
    # Если статус-код не равен 200 (любая ошибка), возвращаем False
    return False


# Основная функция программы
def main():
    # URL API "Звёздные войны" для получения информации о персонаже с ID 1 (Люк Скайуокер)
    url = "https://swapi.dev/api/people/1"
    
    # Вызов функции send_request и сохранение полученных данных
    data = send_request(url)
    
    # Проверяем, что данные были успешно получены (не False)
    if data:
        # Извлечение отдельных значений из словаря data по ключам
        
        # Имя персонажа
        name = data["name"]
        
        # Рост персонажа (в сантиметрах)
        height = data["height"]
        
        # Масса (вес) персонажа (в килограммах)
        mass = data["mass"]
        
        # Цвет волос персонажа
        hair_color = data["hair_color"]
        
        # Вывод полученной информации на экран
        # Используется f-строка для форматированного вывода
        print(f"Имя: {name}\nРост: {height}\nВес: {mass}\nЦвет волос: {hair_color}")
    else:
        # Если данные не получены (функция вернула False)
        print("Ошибка: не удалось получить данные с сервера")


# Стандартная конструкция Python для определения точки входа программы
# Код внутри этого условия выполнится только при прямом запуске файла,
# но не при импорте этого файла как модуля в другой программе
if __name__ == "__main__":
    # Вызов основной функции программы
    main()
